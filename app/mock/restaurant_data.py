def get_restaurants_by_city(city: str, **filters) -> list:
    """根据城市筛选餐厅"""
    time.sleep(random.uniform(0.2, 0.5))
    results = [r for r in RESTAURANTS if r["city"] == city]

    cuisine = filters.get("cuisine")
    if cuisine:
        results = [r for r in results if cuisine in r["cuisine"]]

    price_level = filters.get("price_level")
    if price_level:
        results = [r for r in results if r["price_level"] == price_level]

    return results


def get_nearby_restaurants(hotel_lat: float, hotel_lng: float, radius_km: float = 3, **filters) -> list:
    """基于酒店锚点的周边餐厅推荐 - 空间智能核心"""
    import math
    time.sleep(random.uniform(0.2, 0.4))

    def haversine(lat1, lon1, lat2, lon2):
        earth_radius = 6371
        dlat = math.radians(lat2 - lat1)
        dlon = math.radians(lon2 - lon1)
        a = math.sin(dlat / 2) ** 2 + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2)) * math.sin(dlon / 2) ** 2
        return earth_radius * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a))

    results = []
    for r in RESTAURANTS:
        dist = haversine(hotel_lat, hotel_lng, r["latitude"], r["longitude"])
        if dist <= radius_km:
            item = r.copy()
            item["distance"] = round(dist, 2)
            results.append(item)

    results.sort(key=lambda x: x["distance"])
    return results
