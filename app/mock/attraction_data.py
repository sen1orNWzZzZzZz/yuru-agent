def get_attractions_by_city(city: str, **filters) -> list:
    """根据城市筛选景点"""
    time.sleep(random.uniform(0.2, 0.5))
    results = [a for a in ATTRACTIONS if a["city"] == city]

    category = filters.get("category")
    if category:
        results = [a for a in results if a["category"] == category]

    is_free = filters.get("is_free")
    if is_free is not None:
        results = [a for a in results if a["is_free"] == is_free]

    return results


def get_nearby_attractions(lat: float, lng: float, radius_km: float = 10) -> list:
    """获取周边景点"""
    import math
    time.sleep(random.uniform(0.2, 0.4))

    def haversine(lat1, lon1, lat2, lon2):
        earth_radius = 6371
        dlat = math.radians(lat2 - lat1)
        dlon = math.radians(lon2 - lon1)
        a = math.sin(dlat / 2) ** 2 + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2)) * math.sin(dlon / 2) ** 2
        return earth_radius * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a))

    results = []
    for a in ATTRACTIONS:
        dist = haversine(lat, lng, a["latitude"], a["longitude"])
        if dist <= radius_km:
            item = a.copy()
            item["distance"] = round(dist, 2)
            results.append(item)
    results.sort(key=lambda x: x["distance"])
    return results


def assess_altitude_risk(attractions: list) -> list:
    """评估高反风险"""
    risks = []
    for a in attractions:
        alt = a.get("altitude")
        if alt and alt > 2000:
            if alt > 3500:
                level = "high"
                advice = "海拔较高，建议提前适应，携带氧气瓶，避免剧烈运动"
            elif alt > 2500:
                level = "medium"
                advice = "中等海拔，老人儿童需注意，建议缓慢游览"
            else:
                level = "low"
                advice = "海拔略高，一般人群可正常游览"
            risks.append({
                "attraction_id": a["id"],
                "name": a["name"],
                "altitude": alt,
                "risk_level": level,
                "advice": advice,
            })
    return risks
